package com.pms.persistant.dao;

public class CustomerReportResponseDao {

	private String customerName;
	private String ticketId;
	private String date;
	private String playThing;
	private String time;
	private String balance;

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getTicketId() {
		return ticketId;
	}

	public void setTicketId(String ticketId) {
		this.ticketId = ticketId;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getPlayThing() {
		return playThing;
	}

	public void setPlayThing(String playThing) {
		this.playThing = playThing;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getBalance() {
		return balance;
	}

	public void setPrice(String balance) {
		this.balance = balance;
	}

}
